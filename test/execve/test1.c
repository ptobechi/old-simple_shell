#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>

/**
 * main - execve example
 *
 * Return: Always 0.
 */
int main(void)
{
    char *argv[] = {"/bin/ls", "-l", "/usr/", NULL};

    printf("Before execve\n");
    if (execve(argv[0], argv, NULL) == -1)
    {
        perror("Error:");
    }
	else
		exit(EXIT_SUCCESS);

	printf("After execve\n");
    return (0);
}
